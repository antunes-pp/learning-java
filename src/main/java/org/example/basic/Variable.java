package org.example.basic;

public class Variable {
    public static void main(String[] args) {
        int day = 13;
        int month = 6;
        int year = 2024;

        System.out.println(day);
        System.out.println(month);
        System.out.println(year);

        day = 22;
        month = 12;
        year = 1996;

        System.out.println(day);
        System.out.println(month);
        System.out.println(year);
    }
}
